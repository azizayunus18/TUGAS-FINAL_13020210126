    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
//        jTextField1.disable();
        int i = jTable1.getSelectedRow();
        
        if (i == -1) {
            return;
            
        } else {
            String nim = (String) model.getValueAt(i, 0);
            jTextField1.setText(nim);
            String nama = (String) model.getValueAt(i, 1);
            jTextField2.setText(nama);
            String uh = (String) model.getValueAt(i, 2);
            jTextField3.setText(uh);
            String uts = (String) model.getValueAt(i, 3);
            jTextField4.setText(uts);
            String uas = (String) model.getValueAt(i, 4);
            jTextField5.setText(uas);
            String rata = (String) model.getValueAt(i, 5);
            jTextField6.setText(rata);
            String kompetensi = (String) model.getValueAt(i, 6);
            jTextField7.setText(kompetensi);
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jTextField5KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyReleased
        uh = Integer.parseInt(jTextField3.getText());
        uts = Integer.parseInt(jTextField4.getText());
        uas = Integer.parseInt(jTextField5.getText());
        rata_rata = (uh + uts + uas)/3;
        jTextField6.setText(String.valueOf(rata_rata));
        if (rata_rata < 65) {
            jTextField7.setText("Tidak Kompeten");
        } else if (rata_rata >= 65) {
            jTextField7.setText("Kompeten");
        }
    }//GEN-LAST:event_jTextField5KeyReleased

