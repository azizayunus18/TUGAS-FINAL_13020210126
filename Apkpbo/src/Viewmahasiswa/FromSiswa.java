    public void LoadDataMhs(){
        model = new DefaultTableModel();
        jTable1.setModel(model);
        model.addColumn("NIM");
        model.addColumn("NAMA");
        model.addColumn("UH");
        model.addColumn("UTS");
        model.addColumn("UAS");
        model.addColumn("RATA-RATA");
        model.addColumn("KOMPETENSI");
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            Connection c = (Connection) koneksi.getKoneksi();
            Statement s = (Statement) c.createStatement();
            String sql = "SELECT * FROM data_mhs";
            ResultSetImpl r = (ResultSetImpl) s.executeQuery(sql);
            
            while (r.next()) {
                Object[] o = new Object[7];
                o[0] = r.getString("nim");
                o[1] = r.getString("nama");
                o[2] = r.getString("uh");
                o[3] = r.getString("uts");
                o[4] = r.getString("uas");
                o[5] = r.getString("rata");
                o[6] = r.getString("kompetensi");
                model.addRow(o);
                
            }
            for (int i = 0; i < model.getColumnCount(); i++) {
                TableColumn column = jTable1.getColumnModel().getColumn(i);
                if(i == 0)column.setPreferredWidth(200);
                if(i == 1)column.setPreferredWidth(200);
                if(i == 2)column.setPreferredWidth(100);
                if(i == 3)column.setPreferredWidth(100);
                if(i == 4)column.setPreferredWidth(100);
                if(i == 5)column.setPreferredWidth(100);
                if(i == 6)column.setPreferredWidth(150);
            }
            r.close();
            s.close();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }
